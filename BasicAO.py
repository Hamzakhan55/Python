#  Basic Arithmetic Operations

a = 40
b = 3

print("the value of ",a,"+", b,"is =", a+b)
print("the value of ",a,"-", b,"is =", a-b)
print("the value of ",a,"*", b,"is =", a*b)
print("the value of ",a,"/", b,"is =", a/b)
print("the value of ",a,"//", b,"is =", a//b)
print("the value of ",a,"%", b,"is =", a%b)